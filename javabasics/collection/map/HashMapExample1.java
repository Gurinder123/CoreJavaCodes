package collection.map;

import java.util.HashMap;

public class HashMapExample1 {

	public static void main(String r[]) {
		// Hashmap accepts key value and doesnot takes duplicate key
		HashMap<String, String> hm = new HashMap<>();

		hm.put("1", "Gurinder");
		hm.put("2", "Ravi");
		hm.put("3", "Rahul");
		hm.put("4", "James");
		hm.put("5", "Komal");

	}
}
